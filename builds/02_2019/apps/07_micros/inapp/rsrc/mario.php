<?php

$mario = [
    "     ======     ",
    "    ==========  ",
    "    %%%%##%#    ",
    "   %%#%###%###  ",
    "   %%#%%###%### ",
    "   %%%####%%%%  ",
    "     ########   ",
    "    %%%=%%%     ",
    "   %%%%=%%=%%%  ",
    "  %%%%%====%%%% ",
    "  ###%=#==#=%## ",
    "  ####======### ",
    "  ###========## ",
    "    ==== ====   ",
    "   %%%%   %%%%  ",
    "  %%%%%   %%%%% ",
];

$out = [];
for ($i = 0; $i < 16; $i++)
for ($j = 0; $j < 16; $j++) {

    $b = substr($mario[$i], $j, 1);

    $k = ($j >> 2);
    $a = ($j & 3);

    switch ($b) {
        case ' ': $b = 0; break;
        case '%': $b = 1; break;
        case '=': $b = 2; break;
        case '#': $b = 3; break;
    }

    @ $out[4*$i + $k] |= ($b << (6 - 2*$a));
}

for ($i = 0; $i < 64; $i++) {

    $b = $out[$i];

    echo sprintf("0x%02x, ", $b);
    if (($i % 8) == 7) echo "\n";
}

